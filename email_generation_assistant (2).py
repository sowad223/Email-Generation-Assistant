def metric_fact_inclusion(generated: str, facts: List[str]) -> float:
    if not facts: return 100.0
    total_included = 0
    generated_lower = generated.lower()
    for fact in facts:
        keywords = [word.lower() for word in fact.split() if len(word) > 3]
        if not keywords:
            if fact.lower() in generated_lower: total_included += 1
            continue
        matches = sum(1 for kw in keywords if kw in generated_lower)
        if matches / len(keywords) >= 0.5:
            total_included += 1
    return (total_included / len(facts)) * 100

