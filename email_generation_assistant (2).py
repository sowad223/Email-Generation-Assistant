"""## 1. Prompt Engineering Strategy

We employ a **Role-Playing + Chain-of-Thought + Few-Shot** strategy for the primary model (Model A) to ensure high factual integrity and professional structure.
"""

SYSTEM_PROMPT = """You are an expert professional email writer with 15+ years of experience in corporate communication.
Your emails are always clear, concise, polite, and perfectly tailored to the intent, facts, and tone.

Follow this Chain-of-Thought process:
1. Analyze the Intent, Key Facts, and Tone.
2. Structure the email: Subject, Greeting, Introduction, Body (integrate facts), Call to Action, and Closing.
3. Ensure professional language matching the Tone.
4. Maintain strict conciseness."""

def generate_email_few_shot(intent, facts, tone, model="llama-3.3-70b-versatile"):
    user_prompt = f"Intent: {intent}\nFacts: {facts}\nTone: {tone}\n\nOutput only the email."
    completion = client.chat.completions.create(
        model=model,
        messages=[{"role": "system", "content": SYSTEM_PROMPT}, {"role": "user", "content": user_prompt}],
        temperature=0.7
    )
    return completion.choices[0].message.content.strip()

def generate_email_zero_shot(intent, facts, tone, model="llama-3.1-8b-instant"):
    zero_shot_system = "You are a professional email writer. Output a clear, concise email based on intent, facts, and tone."
    user_prompt = f"Intent: {intent}\nFacts: {facts}\nTone: {tone}\n\nOutput only the email."
    completion = client.chat.completions.create(
        model=model,
        messages=[{"role": "system", "content": zero_shot_system}, {"role": "user", "content": user_prompt}],
        temperature=0.7
    )
    return completion.choices[0].message.content.strip()

"""## 2. Test Data and Custom Metrics

We define 10 scenarios and 3 metrics:
1. **Fact Recall**: % of facts present in the email.
2. **Tone Accuracy**: LLM-as-a-Judge score (0-100).
3. **Conciseness**: Penalty-based score relative to an ideal length.
"""

def metric_fact_recall(generated, facts):
    if not facts: return 100.0
    found = 0
    gen_lower = generated.lower()
    for f in facts:
        kws = [w.lower() for w in f.split() if len(w) > 3]
        if kws and sum(1 for kw in kws if kw in gen_lower) / len(kws) >= 0.8: found += 1
    return (found / len(facts)) * 100.0

def metric_tone_accuracy(generated, tone):
    prompt = f"Rate 0-100 how well this matches {tone} tone. Return only the number. Email: {generated[:200]}"
    resp = client.chat.completions.create(model='llama-3.1-8b-instant', messages=[{'role': 'user', 'content': prompt}])
    try:
        val = float(''.join(filter(str.isdigit, resp.choices[0].message.content))[:3])
        return min(100.0, val)
    except: return 0.0

def metric_conciseness(generated):
    l = len(generated.split())
    score = 100 - (abs(l - 100) * 2)
    return max(0.0, float(score))

def evaluate_email(gen, facts, tone):
    return {
        'fact_recall': metric_fact_recall(gen, facts),
        'tone_accuracy': metric_tone_accuracy(gen, tone),
        'conciseness': metric_conciseness(gen)
    }

